declare module '@mui/material/styles' {
  interface Theme {
    custom: {
      gradients: {
        primary: string;
        secondary: string;
        success: string;
        warning: string;
        error: string;
      };
      shadows: {
        card: string;
        button: string;
        dialog: string;
      };
    };
  }

  interface ThemeOptions {
    custom?: {
      gradients?: {
        primary?: string;
        secondary?: string;
        success?: string;
        warning?: string;
        error?: string;
      };
      shadows?: {
        card?: string;
        button?: string;
        dialog?: string;
      };
    };
  }
}

